int firstNumber = 15;
int secondNumber = 10;

//Operators for those two numbers
Console.WriteLine("SUM : " + (firstNumber + secondNumber));
Console.WriteLine("SUB : " + (firstNumber - secondNumber));
Console.WriteLine("MUL : " + (firstNumber * secondNumber));
Console.WriteLine("DIV : " + (firstNumber / secondNumber));
Console.WriteLine("MOD : " + (firstNumber % secondNumber));

//Bitwise Operators
Console.WriteLine("AND : " + (firstNumber & secondNumber));
Console.WriteLine("OR : " + (firstNumber | secondNumber));
Console.WriteLine("XOR : " + (firstNumber ^ secondNumber));
Console.WriteLine("NOT : " + (~firstNumber));
Console.WriteLine("LEFT SHIFT : " + (firstNumber << 2));
Console.WriteLine("RIGHT SHIFT : " + (firstNumber >> 2));
